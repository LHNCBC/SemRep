simplify(X, Z) :-
	(ground(X) -> true ; raise_exception(simplify(X))),
        simplify_rec(X, Z).
        % print_message(informational, simplifed(Z)).

simplify_rec(sup, +sup) :- !.
simplify_rec(L1..U1, Expr) :- !,
        simplify_rec(L1, L2),
        simplify_rec(U1, U2),
        (   L2 = -sup,
            U2 = +sup
        ->  Expr = true
        ;   L2 = +sup
        ->  Expr = false
        ;   U2 = -sup
        ->  Expr = false
        ;   integer(L2),
            integer(U2),
            L2 > U2
        ->  Expr = false
        ;   Expr = (L2..U2)
        ).
simplify_rec(\ (X1), Expr) :- !,
        simplify_rec(X1, X2),
        (   X2 = true -> Expr = false
        ;   X2 = false -> Expr = true
        ;   Expr = \ (X2)
        ).
simplify_rec(X\Y, Expr) :- !,
        simplify_rec(X, X1),
        simplify_rec(Y, Y1),
        (   Y1 = false -> Expr = X1
        ;   X1 = true  -> Expr = \ (Y1)
        ;   Expr = X1\Y1
        ).
simplify_rec(+sup, +sup) :- !.
simplify_rec(+(X), Expr) :- !,
        simplify_rec(X, Expr).
simplify_rec(-sup, -sup) :- !.
simplify_rec(-(X), Expr) :- !,
        simplify_rec(X, Y),
        (   integer(Y) -> Expr is -Y
        ;   Y = +sup -> Expr = -sup
        ;   Y = -sup -> Expr = +sup
        ;   Y = A-B -> Expr = B-A
        ;   Expr = -(Y)
        ).
simplify_rec(X1*Y1, Expr) :- !,
        simplify_rec(X1, X2),
        simplify_rec(Y1, Y2),
        (   X2 = +sup, pos_expr(Y2) -> Expr = +sup
        ;   X2 = +sup, neg_expr(Y2) -> Expr = -sup
        ;   X2 = -sup, pos_expr(Y2) -> Expr = -sup
        ;   X2 = -sup, neg_expr(Y2) -> Expr = +sup
        ;   Y2 = +sup, pos_expr(X2) -> Expr = +sup
        ;   Y2 = +sup, neg_expr(X2) -> Expr = -sup
        ;   Y2 = -sup, pos_expr(X2) -> Expr = -sup
        ;   Y2 = -sup, neg_expr(X2) -> Expr = +sup
        ;   Expr = X2*Y2
        ).
simplify_rec(X1/>Y1, Expr) :- !, % ceiling
        simplify_rec(X1, X2),
        simplify_rec(Y1, Y2),
        (   X2 = +sup, pos_expr(Y2) -> Expr = +sup
        ;   X2 = +sup, neg_expr(Y2) -> Expr = -sup
        ;   X2 = -sup, pos_expr(Y2) -> Expr = -sup
        ;   X2 = -sup, neg_expr(Y2) -> Expr = +sup
        ;   Y2 = +sup, pos_expr(X2) -> Expr = 1
        ;   Y2 = +sup, neg_expr(X2) -> Expr = 0
        ;   Y2 = -sup, pos_expr(X2) -> Expr = 0
        ;   Y2 = -sup, neg_expr(X2) -> Expr = 1
        ;   Expr = X2/>Y2
        ).
simplify_rec(X1/<Y1, Expr) :- !, % floor
        simplify_rec(X1, X2),
        simplify_rec(Y1, Y2),
        (   X2 = +sup, pos_expr(Y2) -> Expr = +sup
        ;   X2 = +sup, neg_expr(Y2) -> Expr = -sup
        ;   X2 = -sup, pos_expr(Y2) -> Expr = -sup
        ;   X2 = -sup, neg_expr(Y2) -> Expr = +sup
        ;   Y2 = +sup, pos_expr(X2) -> Expr = 0
        ;   Y2 = +sup, neg_expr(X2) -> Expr = -1
        ;   Y2 = -sup, pos_expr(X2) -> Expr = -1
        ;   Y2 = -sup, neg_expr(X2) -> Expr = 0
        ;   Expr = X2/<Y2
        ).
simplify_rec(X1+Y1, Expr) :- !,
        simplify_rec(X1, X2),
        simplify_rec(Y1, Y2),
        (   X2 = +sup -> Expr = +sup
        ;   Y2 = +sup -> Expr = +sup
        ;   X2 = -sup -> Expr = -sup
        ;   Expr = X2+Y2
        ).
simplify_rec(X1-Y1, Expr) :- !,
        simplify_rec(X1, X2),
        simplify_rec(Y1, Y2),
        (   X2 = +sup -> Expr = +sup
        ;   Y2 = +sup -> Expr = -sup
        ;   X2 = -sup -> Expr = -sup
        ;   integer(X2), integer(Y2) -> Expr is X2-Y2
        ;   Expr = X2-Y2
        ).
simplify_rec(max(X1,Y1), Expr) :- !,
        simplify_rec(X1, X2),
        simplify_rec(Y1, Y2),
        (   X2 = +sup -> Expr = X2
        ;   Y2 = +sup -> Expr = Y2
        ;   X2 = -sup -> Expr = Y2
        ;   Y2 = -sup -> Expr = X2
        ;   Expr = max(X2,Y2)
        ).
simplify_rec(min(X1,Y1), Expr) :- !,
        simplify_rec(X1, X2),
        simplify_rec(Y1, Y2),
        (   X2 = +sup -> Expr = Y2
        ;   Y2 = +sup -> Expr = X2
        ;   X2 = -sup -> Expr = X2
        ;   Y2 = -sup -> Expr = Y2
        ;   Expr = min(X2,Y2)
        ).
simplify_rec(X1<Y1, Expr) :- !,
        simplify_rec(X1, X2),
        simplify_rec(Y1, Y2),
        (   X2 = +sup -> Expr = false
        ;   Y2 = +sup -> Expr = true
        ;   Expr = (X2<Y2)
        ).
simplify_rec(X1=<Y1, Expr) :- !,
        simplify_rec(X1, X2),
        simplify_rec(Y1, Y2),
        (   X2 = +sup -> Expr = false
        ;   Y2 = +sup -> Expr = true
        ;   Expr = (X2=<Y2)
        ).
simplify_rec(X1=Y1, (X2=Y2)) :- !,
        simplify_rec(X1, X2),
        simplify_rec(Y1, Y2).
simplify_rec((If -> Then ; Else), Expr) :- !,
        simplify_rec(If, If1),
        simplify_rec(Then, Then1),
        simplify_rec(Else, Else1),
        (   If1 = true -> Expr = Then1
        ;   If1 = false -> Expr = Else1
        ;   Then1 = Else1 -> Expr = Then1
        ;   Expr = (If1 -> Then1 ; Else1)
        ).
simplify_rec(Expr, Expr).

